package scari.corp.taro.bot.command;

import scari.corp.taro.bot.dto.BotResponse;

/**
 * Универсальный контракт для реализации команд чатов.
 * <p>
 * Позволяет динамически обрабатывать входящие сообщения из любых текстовых платформ,
 * изолируя логику конкретного мессенджера от бизнес-логики приложения.
 */
public interface BotCommand {

    /**
     * Проверяет, применима ли данная команда к входящему текстовому запросу.
     *
     * @param input очищенный текстовый ввод от пользователя
     * @return {@code true}, если данный класс умеет обрабатывать этот запрос, иначе {@code false}
     */
    boolean canHandle(String input);

    /**
     * Выполняет бизнес-логику команды и формирует ответ для платформы.
     * <p>
     * Метод работает исключительно со строковыми абстракциями, что исключает
     * прямую зависимость от библиотек конкретных API.
     *
     * @param text          полный текст входящего сообщения
     * @param destinationId уникальный идентификатор чата/получателя на платформе
     * @param username      логин пользователя на веб-сайте (может быть {@code null}, если аккаунт не привязан)
     * @param sessionId     идентификатор изолированной гостевой сессии для хранения временной истории
     * @return универсальный объект {@link BotResponse}, содержащий текст ответа и плоский список экранных кнопок
     */
    BotResponse apply(String text, String destinationId, String username, String sessionId);
}
